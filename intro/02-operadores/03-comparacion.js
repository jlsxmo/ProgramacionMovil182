let a = 10;
console.log(a > 5); // true
console.log(a < 5); // false
console.log(a >= 10); // true
console.log(a <= 10); // true

console.log(a == 10); // true
console.log(a != 10); // false
console.log(a == '10'); // true '==' compara el valor
console.log(a === '10'); // false '===' compara el tipo de dato
console.log(a !== '10'); // true